package logic;

public class Personaj {

        private String nameC;
        private int lvlC;
        private int expirienceC;

        public String getNameC() {
            return nameC;
        }

        public void setNameC(String nameC) {
            this.nameC = nameC;
        }

        public int getLvlC() {
            return lvlC;
        }

        public void setLvlC(int lvlC) {
            this.lvlC = lvlC;
        }

        public int getExpirienceC() {
            return expirienceC;
        }

        public void setExpirienceC(int expirienceC) {
            this.expirienceC = expirienceC;
        }
}
